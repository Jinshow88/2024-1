package com.green.day8.ch4;

public class FlowEx16_3 {
    public static void main(String[] args){
        int star = 10;
        for(int i=1; i<=star/2; i++) {
            for (int e = 1; e <= star; e++) {
                System.out.print("*");
            }
            System.out.println();
        }




        /*
        가로 10개
        세로 5개
        별 출력해주세요.
         */
    }
}
