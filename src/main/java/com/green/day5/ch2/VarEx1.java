package com.green.day5.ch2;

public class VarEx1 {
    public static void main(String[]args){
        int year = 0; //선언후 변수 초기화
        int age = 14;
        System.out.println(year);
        System.out.println(age);

        age = age + 1;
        year = age + 2000;

        System.out.println(year);
        System.out.println(age);
    }
}

